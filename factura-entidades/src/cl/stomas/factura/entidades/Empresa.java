package cl.stomas.factura.entidades;

import java.io.Serializable;

import javax.persistence.Column;
import javax.persistence.Entity;
import javax.persistence.GeneratedValue;
import javax.persistence.GenerationType;
import javax.persistence.Id;
import javax.persistence.SequenceGenerator;
import javax.persistence.Table;

@Entity
@SequenceGenerator(name="seq_empresa", sequenceName="seq_empresa", allocationSize = 1, initialValue = 1)
@Table(name="empresa")
public class Empresa implements Serializable{

	private static final long serialVersionUID = 6162290643424094127L;
	
	@Id
	@GeneratedValue(generator="seq_empresa", strategy = GenerationType.SEQUENCE)
	@Column(name="empresa_id")
	private long id;
	
	@Column(name="nombre")
	private String nombre;
	
	@Column(name="razonSocial")
	private String razonSocial;
	
	@Column(name="rut")
	private String rut;
	
	@Column(name="direccion")
	private String direccion;

	public String getDireccion() {
		return direccion;
	}

	public void setDireccion(String direccion) {
		this.direccion = direccion;
	}

	/**
	 * @return the id
	 */
	public long getId() {
		return id;
	}
	
	/**
	 * @param id the id to set
	 */
	public void setId(long id) {
		this.id = id;
	}

	/**
	 * @return the nombre
	 */
	public String getNombre() {
		return nombre;
	}

	/**
	 * @param nombre the nombre to set
	 */
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}

	/**
	 * @return the razonSocial
	 */
	public String getRazonSocial() {
		return razonSocial;
	}

	/**
	 * @param razonSocial the razonSocial to set
	 */
	public void setRazonSocial(String razonSocial) {
		this.razonSocial = razonSocial;
	}

	/**
	 * @return the rut
	 */
	public String getRut() {
		return rut;
	}

	/**
	 * @param rut the rut to set
	 */
	public void setRut(String rut) {
		this.rut = rut;
	}
	
}
